CREATE TABLE IF NOT EXISTS "TPRESCRIPCIONES" (
	"ID"	INTEGER NOT NULL UNIQUE,
	"INDICACIONES"	TEXT,
	"MEDICAMENTO"	INTEGER,
	"CANTIDAD"	TEXT,
	"INTERVALO"	TEXT,
	"DURACION"	TEXT,
	"FECHA"	TEXT NOT NULL,
	"HORA"	TEXT NOT NULL,
	PRIMARY KEY("ID" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "TMEDICOS";
CREATE TABLE IF NOT EXISTS "TMEDICOS" (
	"ID"	INTEGER NOT NULL UNIQUE,
	"NOMBRE"	TEXT,
	"ESPECIALIDAD"	TEXT,
	PRIMARY KEY("ID")
);
DROP TABLE IF EXISTS "TCITAS";
CREATE TABLE IF NOT EXISTS "TCITAS" (
	"id"	NUMERIC,
	"horario_inicio"	TEXT,
	"horario_fin"	TEXT,
	"ubicacion"	TEXT,
	"contacto"	TEXT,
	"id_paciente"	INTEGER NOT NULL,
	"id_medico"	INTEGER,
	"acudio"	INTEGER,
	"fecha"	TEXT,
	"Informacion"	TEXT,
	"especialidad"	TEXT,
	FOREIGN KEY("id_medico") REFERENCES "TMEDICOS"("ID"),
	FOREIGN KEY("id_paciente") REFERENCES "TPACIENTES"("ID"),
	PRIMARY KEY("id")
);
